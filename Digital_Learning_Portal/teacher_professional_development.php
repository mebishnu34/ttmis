<h1 style="text-align:left">Introduction</h1>
<p class="text_style">
Education has been the subject of intense accountability measures around the world. As measures to inspect
schools and appraise teachers have been introduced under the banner of ‘modernizing’ teaching, teachers have
often felt a lack of ownership and a lack of self-worth (Rudduck, 1991). Within the context of ‘rolling reform’
and piecemeal implementation, the professional development of teachers has become a high-profile, politically
‘hot’ issue.
All teachers are required to engage in professional development; to identify, document, record and
evaluate it as they cross through the barriers of qualified teacher and induction standards, grapple with targets for
performance management, submit threshold applications or bid for research scholarships, international
exchanges, professional bursaries and sabbaticals. (Anne Campbell, Practitioner Research and Professional
Development in Education, 2004)
The education and professional development of every teacher needs to be seen as a lifelong task, and be
structured and resourced accordingly. To equip the teaching body with the skills and competences needed for its
new roles, it is necessary to have both quality initial teacher education and a coherent process of continuous
professional development to keep teachers up to date with the skills required in a knowledge based society
(Hendriks & Luyten & Scheerens & Sleegers & Steen, 2010).
The conceptual clarification on professional development given by Guskey (1999) sounds appropriate:
“those processes and activities designed to enhance the professional knowledge, skills, and attitudes of educators
so that they might, in turn, improve the learning of students … it also involves learning how to redesign
educational structures and cultures” (p.16). He also argues that the deficiency approach (i.e. professional
development activities to contribute to make up one’s deficiencies of knowledge and skills) is itself deficient,
and should cover a wider canvas of continuing professional development (CPD) so as to keep pace with the
emerging knowledge base of the profession and its conceptual and craft skills (Panda, 2009).
The professional development of teachers has been a target of government policy. It is contained within
an official publication, Learning and Teaching: A Strategy for Professional Development (DfES, 2001). There
has been a gradual recognition over the last ten years or so of the importance of continuing professional
development (CPD), as the English government has launched initiative after initiative in schools and teachers
have tried to meet the challenges of rapid change. Literacy and numeracy strategies followed the juggernaut of
the National Curriculum and testing, then for example, education action zones, Excellence in Cities, Beacon
schools, flagship schools, training schools, specialist schools and colleges, city academies and networked
learning communities, to name but a few.
</p>

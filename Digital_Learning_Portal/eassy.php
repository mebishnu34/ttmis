Internet Terms and Description
WWW(World Wide Web)
	The world Wide Web is a system for providing, organizing and accessing a wide variety of resources (text, images and sound) that are available via the Internet.
	European Laboratory for practical Physic created the World Wide Web in the early 1990's. The goal of the World Wide Web was to allow researchers to work together on projects and to make project information easily accessible. The first accessible web site was created in 1991.
HTML (Hypertext Markup Language)
HTML is a set of Standardized codes or tags that defines and describes the structure of a web page and is used to prepare documents for the World Wide Web. In addition, HTML documents are portable-they can be viewed with web browser on any type of computer.
Webpage
A Web Pages is unit of information, often       a document that is available over the World Wide Web. A web page may contain texts, pictures, sound files, videos and the hyperlinks.

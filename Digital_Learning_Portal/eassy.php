HTTP
HTTP (Hyper Text Transfer Protocol) is the set of rules or protocol that enables hypertext data to be transferred from one computer to another. HTTP enables users to retrieve a wide variety of resources such as text, graphics, sounds, animations and other documents in the form of web pages.
Hyperlinks
Hyperlinks are highlighted text or image that are on tab web pages. hyperlinks connect to other pages on the web. Hyperlink  appear underlined and in color on the web page and these can be effortlessly identified.
Web browser
A browse is a software program that acts as an interface between the user and the World Wide Web. The browser sends request for information that is available on the internet and displays the information. There are many different types if browsers. Among them, Internet Explorer is the most known in Nepal.
Search Engine
A Web Search Engine is an interactive tool that enables user to locate information available via the WWW. Search Engine provide "fill-out" forms and other interfaces so that user can type in a query, submit the request and retrieve a list of resources that match that search criteria. The hypertext environment makes it possible to offer a link directly from the list of results to the resources themselves. The |http://www.google.com| is an example of search engine.
Web Browsing/Net Surfing
Internet Browser or net surfing as it is often called in the process of visiting the different websites on the internet hosted by the various Companies, Organizations, Educational Institutions, magazines, Individuals etc.

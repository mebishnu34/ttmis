ISP-Internet Service Provider
	An ISP (Internet Service Provider) or Internet Access Provider is a company that provides the Internet Access facility. Customers can be a business, individuals and organizations. All necessary software is present on the host/server computer and users can access the Internet through this machine. Additional services that can be offered by an ISP include web hosting, domain name service and proprietary online services.

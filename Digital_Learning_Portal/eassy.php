Electronic Mail (E-mail)
	The conventional mailing by post can take several days to travel across the country and weeks to go around the world. That is why now-a-days it is referred to as "snail mail", in recent times, E-mail or Electronic Mail is being used to sand and received massages.
	Email is the transmission of messages and files via a computer network. Email was one of the original features of the Internet enabling scientists and researchers working or government sponsored projects to communicate with their colleges at other locations. Today Email enables administrators, teachers and students to communicate with millions of internet users all over the world. Email is fast becoming is primary communications method for both personal and business use.
	One can create, send/receive, store, print and delete messages using and email program. When you receive an email message, your Internet service provider’s software places the message in your personal mailbox. Your mailbox is a storage location usually residing on the computer that connects you to the Internet such as the server operated by the ISP. A server that contains user’s mailboxes and associated email messages is called a Mail Server. As a standard part of their Internet access services, most ISPs and online services, provide an Internet email program and a mailbox on a mail server.
